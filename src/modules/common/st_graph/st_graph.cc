void STGraph::BackwardExtendStBoundaries() {
  const auto& agents = st_graph_input_->filtered_agents();
  for (const auto& agent : agents) {
    if (nullptr == agent) {
      continue;
    }
    if (!agent->need_backward_extend()) {
      continue;
    }
    // get trajectories which need extend
    const auto it = agent_id_st_boundaries_map_.find(agent->agent_id());
    if (it == agent_id_st_boundaries_map_.end()) {
      continue;
    }
    const auto& st_boundary_ids = it->second;
    const size_t st_boundary_ids_num = st_boundary_ids.size();
    for (size_t i = 0; i < st_boundary_ids_num; ++i) {
      const auto boundary_id = st_boundary_ids[i];
      const auto& it = boundary_id_st_boundaries_map_.find(boundary_id);
      if (it == boundary_id_st_boundaries_map_.end()) {
        continue;
      }
      const auto& st_boundary = it->second;
      if (nullptr == st_boundary) {
        continue;
      }
      const auto idx = boundary_id & 0XFF;
      if (idx < 0) {
        continue;
      }
      std::vector<trajectory::Trajectory> trajectories;
      const auto origin_trajectories_num = agent->trajectories().size();
      const bool use_origin_trajectories = idx < origin_trajectories_num;
      // if (use_origin_trajectories) {
      //   trajectories = agent->trajectories();
      // } else {
      //   trajectories = agent->trajectories();
      // }
      trajectories = agent->trajectories();
      if (trajectories.empty()) {
        continue;
      }
      // const auto& trajectory =
      //     use_origin_trajectories ? trajectories[idx]
      //                             : trajectories[idx -
      //                             origin_trajectories_num];

      const auto& trajectory = trajectories.front();
      if (trajectory.empty()) {
        continue;
      }
      BackwardExtendSingleStBoundary(*agent, trajectory, *st_boundary.get(),
                                     idx);
    }
  }
}

void STGraph::BackwardExtendSingleStBoundary(
    const agent::Agent& agent, const trajectory::Trajectory& trajectory,
    const STBoundary& st_boundary, const size_t idx) {
  const auto time_range = st_graph_input_->time_range();
  const auto path_range = st_graph_input_->path_range();
  const auto& init_point = st_graph_input_->planning_init_point();
  const auto ptr_virtual_lane_manager =
      st_graph_input_->ptr_virtual_lane_manager();
  const bool is_lane_keeping = st_graph_input_->is_lane_keeping();
  const auto ptr_ego_lane = st_graph_input_->ego_lane();
  const auto planned_kd_path = st_graph_input_->processed_path();
  const auto* path_border_querier = st_graph_input_->path_border_querier();
  const double start_absolute_time = st_graph_input_->start_absolute_time();
  const StBoundaryType type = StBoundaryType::EXPAND;
  const double backward_extend_time_s =
      st_graph_input_->backward_extend_time_s();
  const auto& planning_init_point_box =
      st_graph_input_->planning_init_point_box();
  const bool is_rads_scene = st_graph_input_->is_rads_scene();
  const double start_time = std::fmax(st_boundary.max_t(), time_range.first);
  const double end_time =
      std::fmin(start_time + backward_extend_time_s, time_range.second);
  if (start_time > end_time - kMathEpsilon) {
    return;
  }
  const double agent_pred_end_time = trajectory.back().absolute_time();
  double lon_buffer = st_graph_input_->GetSuitableLonBuffer(agent);
  double lat_buffer = st_graph_input_->GetSuitableLateralBuffer(agent);

  // check whether need adjust buffer by t
  bool need_adjust_buffer_by_t = false;
  bool is_parallel = false;
  double nearest_s = 0.0;
  double nearest_l = 0.0;
  const auto& ptr_obj_lane = ptr_virtual_lane_manager->GetNearestLane(
      {agent.x(), agent.y()}, &nearest_s, &nearest_l);
  if (nullptr != ptr_obj_lane) {
    is_parallel =
        st_graph_input_->IsParallelToEgoLane(ptr_obj_lane->get_virtual_id());
    need_adjust_buffer_by_t = StGraphUtils::CheckAdjustLateralBufferByT(
        init_point, ptr_virtual_lane_manager, ptr_ego_lane, ptr_obj_lane, agent,
        is_parallel, is_lane_keeping, is_rads_scene);
  }
  std::vector<std::pair<STPoint, STPoint>> st_point_pairs;
  st_point_pairs.reserve((end_time - start_time) / kTimeResolution);
  constexpr int32_t KBackwardExtendStboundaryStartIdx = 100;
  const int64_t target_boundary_id =
      (agent.agent_id() << 8) + KBackwardExtendStboundaryStartIdx + idx;
  for (double relative_time = start_time; relative_time < end_time;
       relative_time += kTimeResolution) {
    trajectory::TrajectoryPoint point;
    if (start_absolute_time + relative_time < agent_pred_end_time) {
      point = trajectory.Evaluate(start_absolute_time + relative_time);
    } else {
      StGraphUtils::LinearExtendTrajectory(
          trajectory, start_absolute_time + relative_time, &point);
    }
    double specific_lat_buffer = need_adjust_buffer_by_t
                                     ? StGraphUtils::AdjustLateralBufferByT(
                                           point, lat_buffer, ptr_obj_lane)
                                     : lat_buffer;
    Box2d obs_box({point.x(), point.y()}, point.theta(),
                  agent.length() + 2.0 * lon_buffer,
                  agent.width() + 2.0 * specific_lat_buffer);
    // max_s min_s max_l min_l
    std::vector<double> agent_sl_boundary(4);
    std::vector<std::pair<int32_t, Vec2d>> considered_corners;

    StGraphUtils::CalculateAgentSLBoundary(planned_kd_path, obs_box, path_range,
                                           type, &agent_sl_boundary,
                                           &considered_corners);
    double lower_s = std::numeric_limits<double>::max();
    double upper_s = std::numeric_limits<double>::lowest();
    if (StGraphUtils::CalculateSRange(
            planned_kd_path, *path_border_querier, obs_box, type, path_range,
            agent_sl_boundary, considered_corners, planning_init_point_box,
            &lower_s, &upper_s, is_rads_scene)) {
      constexpr double kDistThr = 1e-3;
      if (std::fabs(lower_s - upper_s) < kDistThr) {
        continue;
      }
      st_point_pairs.emplace_back(
          STPoint(lower_s, relative_time, agent.agent_id(), target_boundary_id,
                  point.vel(), point.acc()),
          STPoint(upper_s, relative_time, agent.agent_id(), target_boundary_id,
                  point.vel(), point.acc()));
    }
  }
  if (!st_point_pairs.empty()) {
    std::unique_ptr<STBoundary> st_boundary(new STBoundary(st_point_pairs));
    st_boundary->set_id(target_boundary_id);
    boundary_id_st_boundaries_map_.insert(
        std::make_pair(target_boundary_id, std::move(st_boundary)));
    agent_id_st_boundaries_map_[agent.agent_id()].emplace_back(
        target_boundary_id);
  }
}

bool STGraph::UpdateStBoundaryDecisionResults(
    const std::unordered_map<int64_t, STBoundary::DecisionType>&
        decision_table) {
  if (decision_table.empty()) {
    return true;
  }
  for (const auto& decision_entry : decision_table) {
    auto id = decision_entry.first;
    auto iter = boundary_id_st_boundaries_map_.find(id);
    if (iter == boundary_id_st_boundaries_map_.end()) {
      // TODO: return false; ?
      continue;
    }
    auto* st_boundary = iter->second.get();
    if (st_boundary == nullptr) {
      // TODO: return false; ?
      continue;
    }
    st_boundary->set_decision_type(decision_entry.second);
  }
  const bool is_success = CalculateStPassCorridor();
  return is_success;
}

bool STGraph::CalculateStPassCorridor() {
  if (boundary_id_st_boundaries_map_.empty()) {
    return false;
  }

  const auto& time_range = st_graph_input_->time_range();
  for (size_t i = 0; i < st_pass_corridor_.size(); i++) {
    double t = time_range.first + i * kTimeResolution;
    STPoint upper_point = STPoint::HighestSTPoint();
    STPoint lower_point = STPoint::LowestSTPoint();
    bool find_upper = false;
    bool find_lower = false;
    for (const auto& st_boundary_entry : boundary_id_st_boundaries_map_) {
      const auto& st_boundary = st_boundary_entry.second;
      if (st_boundary == nullptr) {
        continue;
      }
      if (st_boundary->decision_type() ==
          STBoundary::DecisionType::CAUTION_YIELD) {
        continue;
      }
      const auto& decision_type = st_boundary->decision_type();
      if (decision_type == STBoundary::DecisionType::YIELD) {
        STPoint cur_upper_pt, cur_lower_pt;
        if (!st_boundary->GetBoundaryBounds(t, &cur_lower_pt, &cur_upper_pt)) {
          continue;
        }
        if (cur_lower_pt.s() < upper_point.s()) {
          find_upper = true;
          upper_point = cur_lower_pt;
        }
      } else if (decision_type == STBoundary::DecisionType::OVERTAKE) {
        STPoint cur_upper_pt, cur_lower_pt;
        if (!st_boundary->GetBoundaryBounds(t, &cur_lower_pt, &cur_upper_pt)) {
          continue;
        }
        if (cur_upper_pt.s() > lower_point.s()) {
          find_lower = true;
          lower_point = cur_upper_pt;
        }
      }
    }
    if (find_upper) {
      st_pass_corridor_[i].first = upper_point;
    }
    if (find_lower) {
      st_pass_corridor_[i].second = lower_point;
    }
  }
  return true;
}

