void STGraph::MakeStaticAgentStBoundary(
    const agent::Agent& agent, const StBoundaryType type,
    const bool reuse_for_close_pass,
    const double extra_lateral_buffer_for_close_pass) {
  const bool is_large_agent =
      StGraphUtils::IsLargeAgent(agent) && type == StBoundaryType::NORMAL;
  const int32_t reserve_num = st_graph_input_->reserve_num();
  const bool is_lane_keeping = st_graph_input_->is_lane_keeping();
  const auto& init_point = st_graph_input_->planning_init_point();
  const auto& planned_kd_path = st_graph_input_->processed_path();
  const auto ptr_ego_lane = st_graph_input_->ego_lane();
  const auto ptr_virtual_lane_manager =
      st_graph_input_->ptr_virtual_lane_manager();
  const auto* path_border_querier = st_graph_input_->path_border_querier();
  auto mutable_agent_manager = st_graph_input_->mutable_agent_manager();
  const double expand_buffer =
      st_graph_input_->large_agent_expand_param_for_consistency();
  const double small_expand_buffer =
      st_graph_input_->large_agent_small_expand_param_for_consistency();
  const auto& planning_init_point_box =
      st_graph_input_->planning_init_point_box();
  const bool is_rads_scene = st_graph_input_->is_rads_scene();
  if (nullptr == planned_kd_path || reserve_num <= 0 ||
      nullptr == mutable_agent_manager || nullptr == ptr_ego_lane ||
      nullptr == path_border_querier || nullptr == ptr_virtual_lane_manager ||
      nullptr == mutable_agent_manager->mutable_agent(agent.agent_id())) {
    return;
  }
  auto* mutable_agent = mutable_agent_manager->mutable_agent(agent.agent_id());
  auto* mutable_agent_st_info = mutable_agent->mutable_agent_st_info();

  // const std::shared_ptr<VirtualLane> ptr_obj_lane = nullptr;
  double nearest_s = 0.0;
  double nearest_l = 0.0;
  const auto& ptr_obj_lane = ptr_virtual_lane_manager->GetNearestLane(
      {agent.x(), agent.y()}, &nearest_s, &nearest_l);

  // check whether is candicate for close pass
  bool is_candicate_for_close_pass =
      (!reuse_for_close_pass) && (nullptr != ptr_obj_lane) &&
      StGraphUtils::CheckCandicateAgentForClosePass(
          is_lane_keeping, init_point, planned_kd_path, ptr_ego_lane,
          ptr_obj_lane, agent, type, is_rads_scene);

  const bool consider_virtual_time_range =
      (agent.type() == agent::AgentType::VIRTUAL ||
       type == StBoundaryType::EXPAND) &&
      agent.is_time_range_valid();
  const auto& time_range = consider_virtual_time_range
                               ? agent.time_range()
                               : st_graph_input_->time_range();
  const auto& path_range = st_graph_input_->path_range();

  double lon_buffer = st_graph_input_->GetSuitableLonBuffer(agent);

  double lat_buffer = st_graph_input_->GetSuitableLateralBuffer(agent);
  int32_t prev_st_count = 0;
  bool is_in_st_graph = false;
  lat_buffer = StGraphUtils::RecalculateLateralBufferForLargeAgent(
      init_point, *mutable_agent_manager, agent, expand_buffer,
      small_expand_buffer, lat_buffer, &prev_st_count);

  lat_buffer = reuse_for_close_pass
                   ? lat_buffer + extra_lateral_buffer_for_close_pass
                   : lat_buffer;

  auto obs_box = agent.box();
  obs_box.LateralExtend(lat_buffer * 2.0);
  obs_box.LongitudinalExtend(lon_buffer * 2.0);
  // max_s min_s max_l min_l
  std::vector<double> agent_sl_boundary(4);
  std::vector<std::pair<int32_t, Vec2d>> considered_corners;

  StGraphUtils::CalculateAgentSLBoundary(planned_kd_path, obs_box, path_range,
                                         type, &agent_sl_boundary,
                                         &considered_corners);
  std::vector<std::pair<STPoint, STPoint>> st_point_pairs;
  st_point_pairs.reserve(reserve_num);
  double lower_s = std::numeric_limits<double>::max();
  double upper_s = std::numeric_limits<double>::lowest();
  const int64_t boundary_id = (agent.agent_id() << 8) + 0;
  double min_t = std::numeric_limits<double>::max();
  const double max_l = agent_sl_boundary[2];
  const double min_l = agent_sl_boundary[3];
  if (StGraphUtils::CalculateSRange(
          planned_kd_path, *path_border_querier, obs_box, type, path_range,
          agent_sl_boundary, considered_corners, planning_init_point_box,
          &lower_s, &upper_s, is_rads_scene)) {
    for (double t = time_range.first; t < time_range.second;
         t += kTimeResolution) {
      min_t = std::fmin(min_t, t);
      st_point_pairs.emplace_back(
          STPoint(lower_s, t, agent.agent_id(), boundary_id, 0.0, 0.0, min_l),
          STPoint(upper_s, t, agent.agent_id(), boundary_id, 0.0, 0.0, max_l));
    }
  }
  std::vector<int64_t> st_boundaries;
  if (!st_point_pairs.empty()) {
    std::unique_ptr<STBoundary> st_boundary(new STBoundary(st_point_pairs));
    st_boundary->set_id(boundary_id);
    if (type == StBoundaryType::NEIGHBOR) {
      neighbor_boundary_id_st_boundaries_map_.insert(
          std::make_pair(boundary_id, std::move(st_boundary)));
    } else if (type == StBoundaryType::NORMAL) {
      reuse_for_close_pass
          ? close_pass_boundary_id_st_boundaries_map_.insert(
                std::make_pair(boundary_id, std::move(st_boundary)))
          : boundary_id_st_boundaries_map_.insert(
                std::make_pair(boundary_id, std::move(st_boundary)));
    } else if (type == StBoundaryType::EXPAND) {
      expand_boundary_id_st_boundaries_map_.insert(
          std::make_pair(boundary_id, std::move(st_boundary)));
    }
    st_boundaries.emplace_back(boundary_id);
  }
  if (!st_boundaries.empty()) {
    if (type == StBoundaryType::NEIGHBOR) {
      neighbor_agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries;
    } else if (type == StBoundaryType::NORMAL) {
      reuse_for_close_pass
          ? close_pass_agent_id_st_boundaries_map_[agent.agent_id()] =
                st_boundaries
          : agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries;
      is_in_st_graph = true && !reuse_for_close_pass;
    } else if (type == StBoundaryType::EXPAND) {
      expand_agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries;
    }
  } else if (is_candicate_for_close_pass) {
    // the agent has no original st boundary and is candicate for close pass
    static_close_pass_candicate_agent_ids_.insert(agent.agent_id());
  }
  if (is_large_agent && !reuse_for_close_pass) {
    mutable_agent_st_info->is_in_st_graph_ = is_in_st_graph;
    mutable_agent_st_info->lateral_buffer_ = lat_buffer;
    mutable_agent_st_info->st_graph_count_ =
        is_in_st_graph ? ++prev_st_count : 0;
    mutable_agent_st_info->min_t_ = min_t;
  }
}

void STGraph::MakeDynamicAgentStBoundary(
    const agent::Agent& agent, const StBoundaryType type,
    const bool reuse_for_close_pass,
    const double extra_lateral_buffer_for_close_pass) {
  const bool is_large_agent =
      StGraphUtils::IsLargeAgent(agent) && type == StBoundaryType::NORMAL;
  const auto& init_point = st_graph_input_->planning_init_point();
  const auto ptr_virtual_lane_manager =
      st_graph_input_->ptr_virtual_lane_manager();
  const bool is_lane_keeping = st_graph_input_->is_lane_keeping();
  const bool is_in_lane_borrow_status =
      st_graph_input_->is_in_lane_borrow_status();
  const auto ptr_ego_lane = st_graph_input_->ego_lane();
  const int32_t reserve_num = st_graph_input_->reserve_num();
  const auto planned_kd_path = st_graph_input_->processed_path();
  const std::shared_ptr<planning_math::KDPath> ego_motion_simulation_path =
      st_graph_input_->ego_motion_simulation_result()
          ? st_graph_input_->ego_motion_simulation_result()
                ->lat_lon_vehicle_motion_path_ptr
          : nullptr;
  const auto path_border_querier = st_graph_input_->path_border_querier();
  auto mutable_agent_manager = st_graph_input_->mutable_agent_manager();
  const double expand_buffer =
      st_graph_input_->large_agent_expand_param_for_consistency();
  const double small_expand_buffer =
      st_graph_input_->large_agent_small_expand_param_for_consistency();
  const auto& planning_init_point_box =
      st_graph_input_->planning_init_point_box();
  const bool is_rads_scene = st_graph_input_->is_rads_scene();
  if (nullptr == planned_kd_path || nullptr == path_border_querier ||
      reserve_num <= 0 || nullptr == mutable_agent_manager ||
      nullptr == ptr_virtual_lane_manager || nullptr == ptr_ego_lane ||
      nullptr == mutable_agent_manager->mutable_agent(agent.agent_id())) {
    return;
  }
  auto* mutable_agent = mutable_agent_manager->mutable_agent(agent.agent_id());
  auto* mutable_agent_st_info = mutable_agent->mutable_agent_st_info();

  double lon_buffer = st_graph_input_->GetSuitableLonBuffer(agent);

  double lat_buffer = st_graph_input_->GetSuitableLateralBuffer(agent);
  int32_t prev_st_count = 0;
  bool is_in_st_graph = false;
  lat_buffer = StGraphUtils::RecalculateLateralBufferForLargeAgent(
      init_point, *mutable_agent_manager, agent, expand_buffer,
      small_expand_buffer, lat_buffer, &prev_st_count);

  // check whether need adjust buffer by t
  bool need_adjust_buffer_by_t = false;
  bool is_parallel = false;
  bool is_within_ego_lane = false;
  double nearest_s = 0.0;
  double nearest_l = 0.0;
  const auto& ptr_obj_lane = ptr_virtual_lane_manager->GetNearestLane(
      {agent.x(), agent.y()}, &nearest_s, &nearest_l);
  const double half_ego_lane_width = 0.5 * ptr_ego_lane->width_by_s(nearest_s);
  if (nullptr != ptr_obj_lane) {
    is_within_ego_lane =
        ptr_ego_lane->get_virtual_id() == ptr_obj_lane->get_virtual_id() &&
        nearest_l < half_ego_lane_width;
    is_parallel =
        st_graph_input_->IsParallelToEgoLane(ptr_obj_lane->get_virtual_id());
    need_adjust_buffer_by_t = StGraphUtils::CheckAdjustLateralBufferByT(
        init_point, ptr_virtual_lane_manager, ptr_ego_lane, ptr_obj_lane, agent,
        is_parallel, is_lane_keeping, is_rads_scene);
  }

  const bool need_dynamic_buffer =
      StGraphUtils::NeedDynamicBufferForTimeRange(agent) &&
      type == StBoundaryType::NORMAL;
  // check whether is candicate for close pass
  bool is_candicate_for_close_pass =
      (!reuse_for_close_pass) && (nullptr != ptr_obj_lane) &&
      StGraphUtils::CheckCandicateAgentForClosePass(
          is_lane_keeping, init_point, planned_kd_path, ptr_ego_lane,
          ptr_obj_lane, agent, type, is_rads_scene);

  const double start_absolute_time = st_graph_input_->start_absolute_time();
  const auto& time_range =
      type == StBoundaryType::EXPAND && agent.is_time_range_valid()
          ? agent.time_range()
          : st_graph_input_->time_range();
  const auto& path_range = st_graph_input_->path_range();
  // const double obs_diagonal = agent.box().diagonal();
  // const double search_distance = obs_diagonal * 0.5 + kSearchBuffer;

  const auto& trajectories = agent.trajectories();
  // only consider 2s traj to reverse vru within ego_lane
  const bool is_need_truncate_traj =
      agent.is_vru() && agent.is_reverse() && is_within_ego_lane;

  std::vector<int64_t> st_boundaries;
  st_boundaries.reserve(trajectories.size());
  std::vector<int64_t> st_boundaries_ego;
  const double time_horizon = time_range.second;
  double min_t = std::numeric_limits<double>::max();
  constexpr double kDynamicLowerT = 2.0;
  constexpr double kDynamicUpperT = 4.0;
  for (int i = 0; i < trajectories.size(); ++i) {
    if (trajectories[i].empty()) {
      continue;
    }

    const double agent_pred_end_time = trajectories[i].back().absolute_time();
    std::vector<std::pair<STPoint, STPoint>> st_point_pairs;
    st_point_pairs.reserve(reserve_num);
    const int64_t boundary_id =
        (agent.agent_id() << 8) + i + agent.trajectories().size();
    for (double relative_time = time_range.first; relative_time <= time_horizon;
         relative_time += kTimeResolution) {
      // find path_border_segments which have collison risk
      trajectory::TrajectoryPoint point;
      if (start_absolute_time + relative_time < agent_pred_end_time) {
        if (is_need_truncate_traj) {
          point = trajectories[i].Evaluate(std::fmin(
              start_absolute_time + relative_time, kConsideredReverseVruTime));
        } else {
          point = trajectories[i].Evaluate(start_absolute_time + relative_time);
        }
      } else {
        if (is_need_truncate_traj) {
          point = trajectories[i].Evaluate(kConsideredReverseVruTime);
        } else {
          StGraphUtils::LinearExtendTrajectory(
              trajectories[i], start_absolute_time + relative_time, &point);
        }
      }
      double specific_lat_buffer = need_adjust_buffer_by_t
                                       ? StGraphUtils::AdjustLateralBufferByT(
                                             point, lat_buffer, ptr_obj_lane)
                                       : lat_buffer;

      specific_lat_buffer =
          need_dynamic_buffer
              ? StGraphUtils::CalculateLateralBufferForTimeRange(
                    0.0, specific_lat_buffer, kDynamicLowerT, kDynamicUpperT,
                    relative_time)
              : specific_lat_buffer;
      specific_lat_buffer =
          reuse_for_close_pass
              ? specific_lat_buffer + extra_lateral_buffer_for_close_pass
              : specific_lat_buffer;

      Box2d obs_box(Vec2d(point.x(), point.y()), point.theta(),
                    agent.length() + 2.0 * lon_buffer,
                    agent.width() + 2.0 * specific_lat_buffer);

      // max_s min_s max_l min_l
      std::vector<double> agent_sl_boundary(4);
      std::vector<std::pair<int32_t, Vec2d>> considered_corners;

      StGraphUtils::CalculateAgentSLBoundary(
          planned_kd_path, obs_box, path_range, type, &agent_sl_boundary,
          &considered_corners);
      const double max_l = agent_sl_boundary[2];
      const double min_l = agent_sl_boundary[3];
      double lower_s = std::numeric_limits<double>::max();
      double upper_s = std::numeric_limits<double>::lowest();
      if (StGraphUtils::CalculateSRange(
              planned_kd_path, *path_border_querier, obs_box, type, path_range,
              agent_sl_boundary, considered_corners, planning_init_point_box,
              &lower_s, &upper_s, is_rads_scene)) {
        min_t = std::fmin(min_t, relative_time);
        st_point_pairs.emplace_back(
            STPoint(lower_s, relative_time, agent.agent_id(), boundary_id,
                    point.vel(), point.acc(), min_l),
            STPoint(upper_s, relative_time, agent.agent_id(), boundary_id,
                    point.vel(), point.acc(), max_l));
      }
    }

    if (!st_point_pairs.empty()) {
      std::unique_ptr<STBoundary> st_boundary(new STBoundary(st_point_pairs));
      st_boundary->set_id(boundary_id);
      st_boundaries.emplace_back(boundary_id);
      if (type == StBoundaryType::NEIGHBOR) {
        neighbor_boundary_id_st_boundaries_map_.insert(
            std::make_pair(boundary_id, std::move(st_boundary)));
      } else if (type == StBoundaryType::NORMAL) {
        reuse_for_close_pass
            ? close_pass_boundary_id_st_boundaries_map_.insert(
                  std::make_pair(boundary_id, std::move(st_boundary)))
            : boundary_id_st_boundaries_map_.insert(
                  std::make_pair(boundary_id, std::move(st_boundary)));
      } else if (type == StBoundaryType::EXPAND) {
        expand_boundary_id_st_boundaries_map_.insert(
            std::make_pair(boundary_id, std::move(st_boundary)));
      }
    }
  }

  if (!st_boundaries.empty()) {
    if (type == StBoundaryType::NEIGHBOR) {
      neighbor_agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries;
    } else if (type == StBoundaryType::NORMAL) {
      reuse_for_close_pass
          ? close_pass_agent_id_st_boundaries_map_[agent.agent_id()] =
                st_boundaries
          : agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries;
      is_in_st_graph = true && !reuse_for_close_pass;
    } else if (type == StBoundaryType::EXPAND) {
      expand_agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries;
    }
  } else if (is_candicate_for_close_pass) {
    // the agent has no original st boundary and is candicate for close pass
    dynamic_close_pass_candicate_agent_ids_.insert(agent.agent_id());
  }

  for (int i = 0; i < trajectories.size(); ++i) {
    if (!ego_motion_simulation_path) {
      break;
    }

    if (trajectories[i].empty()) {
      continue;
    }

    if (!is_lane_keeping) {
      break;
    }

    if (is_in_lane_borrow_status) {
      break;
    }

    if (agent.is_static()) {
      break;
    }

    const double agent_pred_end_time = trajectories[i].back().absolute_time();
    std::vector<std::pair<STPoint, STPoint>> st_point_pairs;
    st_point_pairs.reserve(reserve_num);
    const int64_t boundary_id =
        (agent.agent_id() << 8) + i + agent.trajectories().size();
    if (boundary_id_st_boundaries_map_.find(boundary_id) !=
        boundary_id_st_boundaries_map_.end()) {
      continue;
    }
    for (double relative_time = time_range.first; relative_time <= time_horizon;
         relative_time += kTimeResolution) {
      // find path_border_segments which have collison risk
      trajectory::TrajectoryPoint point;
      if (start_absolute_time + relative_time < agent_pred_end_time) {
        if (is_need_truncate_traj) {
          point = trajectories[i].Evaluate(std::fmin(
              start_absolute_time + relative_time, kConsideredReverseVruTime));
        } else {
          point = trajectories[i].Evaluate(start_absolute_time + relative_time);
        }
      } else {
        if (is_need_truncate_traj) {
          point = trajectories[i].Evaluate(kConsideredReverseVruTime);
        } else {
          StGraphUtils::LinearExtendTrajectory(
              trajectories[i], start_absolute_time + relative_time, &point);
        }
      }
      double specific_lat_buffer = need_adjust_buffer_by_t
                                       ? StGraphUtils::AdjustLateralBufferByT(
                                             point, lat_buffer, ptr_obj_lane)
                                       : lat_buffer;

      specific_lat_buffer =
          need_dynamic_buffer
              ? StGraphUtils::CalculateLateralBufferForTimeRange(
                    0.0, specific_lat_buffer, kDynamicLowerT, kDynamicUpperT,
                    relative_time)
              : specific_lat_buffer;

      Box2d obs_box(Vec2d(point.x(), point.y()), point.theta(),
                    agent.length() + 2.0 * lon_buffer,
                    agent.width() + 2.0 * specific_lat_buffer);

      // max_s min_s max_l min_l
      std::vector<double> agent_sl_boundary(4);
      std::vector<std::pair<int32_t, Vec2d>> considered_corners;

      bool is_success = StGraphUtils::CalculateAgentSLBoundary(
          ego_motion_simulation_path, obs_box, &agent_sl_boundary,
          &considered_corners);
      if (!is_success) {
        continue;
      }
      const double max_l = agent_sl_boundary[2];
      const double min_l = agent_sl_boundary[3];
      double lower_s = std::numeric_limits<double>::max();
      double upper_s = std::numeric_limits<double>::lowest();
      if (StGraphUtils::CalculateSRange(
              ego_motion_simulation_path, *path_border_querier, obs_box, type,
              path_range, agent_sl_boundary, considered_corners,
              planning_init_point_box, &lower_s, &upper_s, is_rads_scene)) {
        min_t = std::fmin(min_t, relative_time);
        st_point_pairs.emplace_back(
            STPoint(lower_s, relative_time, agent.agent_id(), boundary_id,
                    point.vel(), point.acc(), min_l),
            STPoint(upper_s, relative_time, agent.agent_id(), boundary_id,
                    point.vel(), point.acc(), max_l));
      }
    }

    if (!st_point_pairs.empty()) {
      std::unique_ptr<STBoundary> st_boundary(new STBoundary(st_point_pairs));
      st_boundary->set_id(boundary_id);
      // st_boundary->set_decision_type(STBoundary::DecisionType::NEIGHBOR_YIELD);
      st_boundaries_ego.emplace_back(boundary_id);
      neighbor_boundary_id_st_boundaries_map_.insert(
          std::make_pair(boundary_id, std::move(st_boundary)));
    }
  }

  if (!st_boundaries_ego.empty()) {
    neighbor_agent_id_st_boundaries_map_[agent.agent_id()] = st_boundaries_ego;
  }

  if (is_large_agent && !reuse_for_close_pass) {
    mutable_agent_st_info->is_in_st_graph_ = is_in_st_graph;
    mutable_agent_st_info->lateral_buffer_ = lat_buffer;
    mutable_agent_st_info->st_graph_count_ =
        is_in_st_graph ? ++prev_st_count : 0;
    mutable_agent_st_info->min_t_ = min_t;
  }
}

void STGraph::MakeStPointsTable() {
  const int32_t reserve_num = st_graph_input_->reserve_num();
  if (reserve_num <= 0) {
    return;
  }
  const auto& time_range = st_graph_input_->time_range();
  st_points_table_.resize(reserve_num);
  for (auto it = boundary_id_st_boundaries_map_.begin();
       it != boundary_id_st_boundaries_map_.end(); ++it) {
    auto st_boundary = it->second.get();
    if (st_boundary->decision_type() ==
        STBoundary::DecisionType::CAUTION_YIELD) {
      continue;
    }
    const auto& lower_points = st_boundary->lower_points();
    const auto& upper_points = st_boundary->upper_points();
    for (int i = 0; i < lower_points.size(); ++i) {
      if (lower_points[i].valid() && upper_points[i].valid()) {
        int index = std::round((lower_points[i].t() - time_range.first) /
                               kTimeResolution);
        st_points_table_[index].emplace_back(lower_points[i], upper_points[i]);
      }
    }
  }

  auto cmp = [](const std::pair<STPoint, STPoint> ele1,
                const std::pair<STPoint, STPoint> ele2) {
    return ele1.first.s() < ele2.first.s();
  };

  for (auto& intervals : st_points_table_) {
    if (intervals.empty()) {
      continue;
    }

    // sort intervals
    std::sort(intervals.begin(), intervals.end(), cmp);

    // merge intervals
    std::vector<std::pair<STPoint, STPoint>> merged_intervals;
    for (int i = 0; i < intervals.size(); ++i) {
      double L = intervals[i].first.s(), R = intervals[i].second.s();
      if (merged_intervals.empty() || merged_intervals.back().second.s() < L) {
        merged_intervals.emplace_back(intervals[i]);
      } else {
        if (merged_intervals.back().second.s() < R) {
          merged_intervals.back().second = intervals[i].second;
        }
      }
    }
    intervals = merged_intervals;
  }
}

void STGraph::ConstructDefaultStPassCorridor() {
  const auto& time_range = st_graph_input_->time_range();
  int32_t total_size =
      int32_t((time_range.second - time_range.first) / kTimeResolution) + 1;
  st_pass_corridor_.reserve(total_size);
  neighbor_corridor_.reserve(total_size);

  STPoint init_upper_point;
  init_upper_point.set_agent_id(kNoAgentId);
  init_upper_point.set_s(std::numeric_limits<double>::max());
  init_upper_point.set_velocity(std::numeric_limits<double>::max());
  init_upper_point.set_acceleration(std::numeric_limits<double>::max());

  STPoint init_lower_point;
  init_lower_point.set_agent_id(kNoAgentId);
  init_lower_point.set_s(std::numeric_limits<double>::lowest());
  init_lower_point.set_velocity(std::numeric_limits<double>::lowest());
  init_lower_point.set_acceleration(std::numeric_limits<double>::lowest());

  for (double t = time_range.first; t < time_range.second + kMathEpsilon;
       t += kTimeResolution) {
    init_upper_point.set_t(t);
    init_lower_point.set_t(t);
    st_pass_corridor_.emplace_back(init_upper_point, init_lower_point);
    neighbor_corridor_.emplace_back(init_upper_point, init_lower_point);
  }
}

