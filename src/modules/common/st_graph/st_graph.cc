bool STGraph::UpdateNeighborAgentResults(
    const std::unordered_map<int32_t, STBoundary::DecisionType>&
        neighbor_agent_decision_table) {
  if (neighbor_agent_decision_table.empty()) {
    return true;
  }
  for (const auto& neighbor_decision_entry : neighbor_agent_decision_table) {
    auto agent_id = neighbor_decision_entry.first;
    auto decision = neighbor_decision_entry.second;
    auto id_iter = neighbor_agent_id_st_boundaries_map_.find(agent_id);
    if (id_iter == neighbor_agent_id_st_boundaries_map_.end()) {
      continue;
    }
    auto agent_st_boundary_ids = id_iter->second;
    if (agent_st_boundary_ids.empty()) {
      continue;
    }
    for (const auto boundary_id : agent_st_boundary_ids) {
      auto boundary_iter =
          neighbor_boundary_id_st_boundaries_map_.find(boundary_id);
      if (boundary_iter == neighbor_boundary_id_st_boundaries_map_.end()) {
        continue;
      }
      auto* st_boundary = boundary_iter->second.get();
      if (st_boundary == nullptr) {
        continue;
      }
      st_boundary->set_decision_type(decision);
    }
  }

  const bool is_success = CalculateNeighborCorridor();
  return is_success;
}

bool STGraph::UpdateNeighborAgentResultsForEgoMotionSimPath() {
  const bool is_success = CalculateNeighborCorridor();
  return is_success;
}

bool STGraph::CalculateNeighborCorridor() {
  if (neighbor_boundary_id_st_boundaries_map_.empty()) {
    return false;
  }

  ResetNeighborCorridor();

  NeighborCorridorYieldInfo neighbor_corridor_yield_info;
  NeighborCorridorOvertakeInfo neighbor_corridor_overtake_info;
  const auto& time_range = st_graph_input_->time_range();
  for (size_t i = 0; i < neighbor_corridor_.size(); i++) {
    double t = time_range.first + i * kTimeResolution;
    STPoint upper_point = STPoint::HighestSTPoint();
    STPoint lower_point = STPoint::LowestSTPoint();
    bool find_upper = false;
    bool find_lower = false;

    for (const auto& st_boundary_entry :
         neighbor_boundary_id_st_boundaries_map_) {
      const auto& st_boundary = st_boundary_entry.second;
      if (st_boundary == nullptr) {
        continue;
      }
      const auto& decision_type = st_boundary->decision_type();
      if (decision_type == STBoundary::DecisionType::NEIGHBOR_YIELD) {
        STPoint cur_upper_pt, cur_lower_pt;
        if (!st_boundary->GetBoundaryBounds(t, &cur_lower_pt, &cur_upper_pt)) {
          continue;
        }
        if (cur_lower_pt.s() < upper_point.s()) {
          find_upper = true;
          upper_point = cur_lower_pt;
        }
      } else if (decision_type == STBoundary::DecisionType::NEIGHBOR_OVERTAKE) {
        STPoint cur_upper_pt, cur_lower_pt;
        if (!st_boundary->GetBoundaryBounds(t, &cur_lower_pt, &cur_upper_pt)) {
          continue;
        }
        if (cur_upper_pt.s() > lower_point.s()) {
          find_lower = true;
          lower_point = cur_upper_pt;
        }
      }
    }
    if (find_upper) {
      neighbor_corridor_[i].first = upper_point;
      if (i < neighbor_corridor_yield_info.first_yield_index) {
        neighbor_corridor_yield_info.first_yield_index = i;
        neighbor_corridor_yield_info.first_yield_agent_id =
            upper_point.agent_id();
        neighbor_corridor_yield_info.first_yield_st_point = upper_point;
      }
    }
    if (find_lower) {
      neighbor_corridor_[i].second = lower_point;
      if (i < neighbor_corridor_overtake_info.first_overtake_index) {
        neighbor_corridor_overtake_info.first_overtake_index = i;
        neighbor_corridor_overtake_info.first_overtake_agent_id =
            lower_point.agent_id();
        neighbor_corridor_overtake_info.first_overtake_st_point = lower_point;
      }
    }
  }

  if (neighbor_corridor_yield_info.first_yield_agent_id !=
      agent::AgentDefaultInfo::kNoAgentId) {
    const auto& first_yield_st_point =
        neighbor_corridor_yield_info.first_yield_st_point;
    neighbor_corridor_yield_info_map_[first_yield_st_point.s()] =
        neighbor_corridor_yield_info;
  }

  if (neighbor_corridor_overtake_info.first_overtake_agent_id !=
      agent::AgentDefaultInfo::kNoAgentId) {
    const auto& first_overtake_st_point =
        neighbor_corridor_overtake_info.first_overtake_st_point;
    neighbor_corridor_overtake_info_map_[first_overtake_st_point.s()] =
        neighbor_corridor_overtake_info;
  }

  // refresh final yield or overtake index and agent id
  if (!neighbor_corridor_yield_info_map_.empty()) {
    const auto [closet_s_to_ego, neighbor_corridor_yield_info] =
        *neighbor_corridor_yield_info_map_.begin();
    neighbor_corridor_[neighbor_corridor_yield_info.first_yield_index].first =
        neighbor_corridor_yield_info.first_yield_st_point;
    first_neighbor_yield_index_ =
        neighbor_corridor_yield_info.first_yield_index;
    first_neighbor_yield_agent_id_ =
        neighbor_corridor_yield_info.first_yield_agent_id;
  }

  if (!neighbor_corridor_overtake_info_map_.empty()) {
    const auto [closet_s_to_ego, neighbor_corridor_overtake_info] =
        *neighbor_corridor_overtake_info_map_.begin();
    neighbor_corridor_[neighbor_corridor_overtake_info.first_overtake_index]
        .first = neighbor_corridor_overtake_info.first_overtake_st_point;
    first_neighbor_overtake_index_ =
        neighbor_corridor_overtake_info.first_overtake_index;
    first_neighbor_overtake_agent_id_ =
        neighbor_corridor_overtake_info.first_overtake_agent_id;
  }

  return true;
}

void STGraph::AddStGraphDataToProto() {
  auto& debug_info_pb = DebugInfoManager::GetInstance().GetDebugInfoPb();
  const auto& path_range = st_graph_input_->path_range();
  const auto& time_range = st_graph_input_->time_range();
  const auto& boundary_id_st_boundaries_map = boundary_id_st_boundaries_map_;
  const auto& neighbor_boundary_id_st_boundaries_map =
      neighbor_boundary_id_st_boundaries_map_;

  auto mutable_st_graph_data = debug_info_pb->mutable_st_graph_data();

  st_graph_data_pb_.set_start_path(path_range.first);
  st_graph_data_pb_.set_end_path(path_range.second);
  st_graph_data_pb_.set_start_time(time_range.first);
  st_graph_data_pb_.set_end_time(time_range.second);

  for (auto it = boundary_id_st_boundaries_map.begin();
       it != boundary_id_st_boundaries_map.end(); ++it) {
    auto* boundary = st_graph_data_pb_.add_st_boundaries();
    boundary->set_boundary_id(it->first);
    auto lower_points = it->second->lower_points();
    auto upper_points = it->second->upper_points();
    for (int i = 0; i < lower_points.size(); ++i) {
      auto* point = boundary->add_lower_points();
      point->set_s(lower_points.at(i).s());
      point->set_t(lower_points.at(i).t());
      point->set_vel(lower_points.at(i).velocity());
      point->set_acc(lower_points.at(i).acceleration());
      point->set_valid(lower_points.at(i).valid());
      point->set_agent_id(lower_points.at(i).agent_id());
    }
    for (int i = 0; i < upper_points.size(); ++i) {
      auto* point = boundary->add_upper_points();
      point->set_s(upper_points.at(i).s());
      point->set_t(upper_points.at(i).t());
      point->set_vel(upper_points.at(i).velocity());
      point->set_acc(upper_points.at(i).acceleration());
      point->set_valid(upper_points.at(i).valid());
      point->set_agent_id(upper_points.at(i).agent_id());
    }
  }

  for (auto it = neighbor_boundary_id_st_boundaries_map.begin();
       it != neighbor_boundary_id_st_boundaries_map.end(); ++it) {
    auto* boundary = st_graph_data_pb_.add_neighbor_st_boundaries();
    boundary->set_boundary_id(it->first);
    auto lower_points = it->second->lower_points();
    auto upper_points = it->second->upper_points();
    for (int i = 0; i < lower_points.size(); ++i) {
      auto* point = boundary->add_lower_points();
      point->set_s(lower_points.at(i).s());
      point->set_t(lower_points.at(i).t());
      point->set_vel(lower_points.at(i).velocity());
      point->set_acc(lower_points.at(i).acceleration());
      point->set_valid(lower_points.at(i).valid());
      point->set_agent_id(lower_points.at(i).agent_id());
    }
    for (int i = 0; i < upper_points.size(); ++i) {
      auto* point = boundary->add_upper_points();
      point->set_s(upper_points.at(i).s());
      point->set_t(upper_points.at(i).t());
      point->set_vel(upper_points.at(i).velocity());
      point->set_acc(upper_points.at(i).acceleration());
      point->set_valid(upper_points.at(i).valid());
      point->set_agent_id(upper_points.at(i).agent_id());
    }
  }

  const auto processed_path = st_graph_input_->processed_path();
  if (nullptr != processed_path) {
    const auto& path_points = processed_path->path_points();
    for (const auto& point : path_points) {
      auto* p = st_graph_data_pb_.add_planned_path();
      p->set_x(point.x());
      p->set_y(point.y());
    }
  }
  mutable_st_graph_data->CopyFrom(st_graph_data_pb_);
}

